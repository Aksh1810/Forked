// Regenerates packages/shared/src/openings.gen.ts from the lichess-org
// chess-openings dataset (CC0). Run: node scripts/build-openings.mjs
import { writeFileSync } from 'node:fs'
import { Chess, castlingSide } from 'chessops/chess'
import { parseSan } from 'chessops/san'
import { makeUci } from 'chessops/util'

// Must match standardUci in packages/shared/src/pgn.ts: castling is stored
// as standard UCI (e1g1), not chessops king-takes-rook form (e1h1).
const standardUci = (pos, move) => {
  const side = castlingSide(pos, move)
  if (side && 'from' in move) {
    return makeUci({ from: move.from, to: (move.from & ~7) | (side === 'h' ? 6 : 2) })
  }
  return makeUci(move)
}

const FILES = ['a', 'b', 'c', 'd', 'e']
const BASE = 'https://raw.githubusercontent.com/lichess-org/chess-openings/master'

const entries = new Map()
let maxPlies = 0

for (const f of FILES) {
  const res = await fetch(`${BASE}/${f}.tsv`)
  if (!res.ok) throw new Error(`fetch ${f}.tsv: ${res.status}`)
  const text = await res.text()
  for (const line of text.split('\n').slice(1)) {
    if (!line.trim()) continue
    const [eco, name, pgn] = line.split('\t')
    const tokens = pgn.split(/\s+/).filter((t) => !/^\d+\.+$/.test(t))
    const pos = Chess.default()
    const ucis = []
    for (const tok of tokens) {
      const move = parseSan(pos, tok)
      if (!move) throw new Error(`illegal san "${tok}" in ${eco} ${name}`)
      ucis.push(standardUci(pos, move))
      pos.play(move)
    }
    entries.set(ucis.join(' '), [eco, name])
    maxPlies = Math.max(maxPlies, ucis.length)
  }
}

const sorted = [...entries.entries()].sort(([a], [b]) => (a < b ? -1 : 1))
const body = sorted.map(([k, [eco, name]]) => `  ${JSON.stringify(k)}: [${JSON.stringify(eco)}, ${JSON.stringify(name)}],`).join('\n')

writeFileSync(
  new URL('../packages/shared/src/openings.gen.ts', import.meta.url),
  `// GENERATED by scripts/build-openings.mjs from lichess-org/chess-openings (CC0).
// Keys are space-joined UCI move prefixes; values are [eco, name]. Do not edit.
export const MAX_OPENING_PLIES = ${maxPlies}

export const OPENINGS: Record<string, readonly [string, string] | undefined> = {
${body}
}
`,
)
console.log(`wrote ${sorted.length} openings, max ${maxPlies} plies`)
